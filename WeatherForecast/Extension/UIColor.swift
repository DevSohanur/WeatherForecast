import UIKit

extension UIColor {
    
    static var rainyBackground: UIColor {
        return UIColor(red: (1/255), green: (77/255), blue: (137/255), alpha: 1.0)
    }
    
    static var sunnyBackground: UIColor {
        return UIColor(red: (1/255), green: (205/255), blue: (246/255), alpha: 1.0)
    }
    
    static var cloudyBackground: UIColor {
        return UIColor(red: (255 / 255), green: (180/255), blue: (47/255), alpha: 1.0)
    }
}
